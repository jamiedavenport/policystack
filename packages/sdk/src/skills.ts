/**
 * Render every file of the skill-pack plugin. Paths are relative to the
 * repository root; deterministic and sorted so the snapshot is stable.
 */
export function renderSkillPack(): SkillFile[] {
	return [
		{ path: ".claude-plugin/marketplace.json", content: marketplaceManifest() },
		{ path: "plugin/.claude-plugin/plugin.json", content: pluginManifest() },
		{ path: "plugin/skills/policystack-audit/SKILL.md", content: auditSkill() },
		{ path: "plugin/skills/policystack-init/SKILL.md", content: initSkill() },
		{
			path: "plugin/skills/policystack-instrument/SKILL.md",
			content: instrumentSkill(),
		},
		{
			path: "plugin/skills/policystack-jurisdiction/SKILL.md",
			content: jurisdictionSkill(),
		},
	];
}
